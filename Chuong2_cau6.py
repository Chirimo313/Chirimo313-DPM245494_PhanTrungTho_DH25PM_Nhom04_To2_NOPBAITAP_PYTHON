#Câu 6: Trình bày ý nghĩa toán tử /, //, %, **, and, or, is
'''
/ : Toán tử chia thực
Thực hiện phép chia và luôn trả về số thực (float), kể cả khi chia hết.

// : Toán tử chia lấy phần nguyên
Thực hiện phép chia và làm tròn xuống (lấy phần nguyên của kết quả)
Kết quả là số nguyên nếu cả hai toán hạng là số nguyên, hoặc số thực nếu có ít nhất một số thực

% : Toán tử chia lấy dư
Trả về phần dư của phép chia

** : Toán tử lũy thừa
Dùng để tính lũy thừa (mũ) của một số

and : Toán tử logic AND
Trả về True nếu cả hai điều kiện đều đúng, ngược lại trả về False

or : Toán tử logic OR
Trả về True nếu ít nhất một trong hai điều kiện đúng, chỉ trả về False khi cả hai đều sai

is : Toán tử so sánh danh tính
Dùng để so sánh xem hai biến có cùng tham chiếu đến một đối tượng trong bộ nhớ không
Không giống == (so sánh giá trị), is so sánh danh tính đối tượng


'''